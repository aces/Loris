namespace LORIS\Data\Models;

/**
 * This class defines an ImageDTO which is an immutable representation of a
 * Image object. Its purpose is to provide accessors to an Image properties.
 *
 * @category Imaging
 * @package  Main
 * @author   Xavier Lecours <xavier.lecours@mcin.ca>
 * @license  http://www.gnu.org/licenses/gpl-3.0.txt GPLv3
 * @link     https://www.github.com/aces/Loris/
 */
class ImageDTO implements
    \LORIS\Data\DataInstance,
    \LORIS\StudyEntities\SiteHaver
{

    private $fileid;

    private $filename;

    private $filelocation;

    private $outputtype;

    private $acquisitionprotocol;

    private $filetype;

    private \CenterID $centerid;

    private $entitytype;

    private $check_nii_filename;

    private $check_bval_filename;

    private $check_bvec_filename;

    private $bids_json_filename;

    /**
     * Constructor
     *
     * @param ?int    $fileid              The FileID
     * @param ?string $filename            The image filename
     * @param ?string $filelocation        The image location
     * @param ?string $outputtype          The output type
     * @param ?string $acquisitionprotocol The aquisition protocol
     * @param ?string $filetype            The file type
     * @param \CenterID $centerid          The image session's centerid
     * @param ?string $entitytype          The image candidate's entity_type
     * @param ?string $check_nii_filename  The NIfTI file generated by mnc2nii
     * @param ?string $check_bval_filename The NIfTI bval file for DWI
     * @param ?string $check_bvec_filename The NIfTI bvec file for DWI
     * @param ?string $bids_json_filename  The BIDS JSON file
     */
    public function __construct(
        ?int $fileid,
        ?string $filename,
        ?string $filelocation,
        ?string $outputtype,
        ?string $acquisitionprotocol,
        ?string $filetype,
        \CenterID $centerid,
        ?string $entitytype,
        ?string $check_nii_filename,
        ?string $check_bval_filename,
        ?string $check_bvec_filename,
        ?string $bids_json_filename
    ) {
        $this->fileid       = $fileid;
        $this->filename     = $filename;
        $this->filelocation = $filelocation;
        $this->outputtype   = $outputtype;
        $this->acquisitionprotocol = $acquisitionprotocol;
        $this->filetype            = $filetype;
        $this->centerid            = $centerid;
        $this->entitytype          = $entitytype;
        $this->check_nii_filename  = $check_nii_filename;
        $this->check_bval_filename = $check_bval_filename;
        $this->check_bvec_filename = $check_bvec_filename;
        $this->bids_json_filename  = $bids_json_filename;
    }

    /**
     * Accessor for fileid.
     *
     * @return ?int
     */
    public function getFileid(): ?int
    {
        return $this->fileid;
    }

    /**
     * Accessor for filename.
     *
     * @return ?string
     */
    public function getFilename(): ?string
    {
        return $this->filename;
    }

    /**
     * Accessor for filelocation.
     *
     * @return ?string
     */
    public function getFilelocation(): ?string
    {
        return $this->filelocation;
    }

    /**
     * Accessor for outputtype.
     *
     * @return ?string
     */
    public function getOutputtype(): ?string
    {
        return $this->outputtype;
    }

    /**
     * Accessor for acquisitionprotocol.
     *
     * @return ?string
     */
    public function getAcquisitionprotocol(): ?string
    {
        return $this->acquisitionprotocol;
    }

    /**
     * Accessor for filetype.
     *
     * @return ?string
     */
    public function getFiletype(): ?string
    {
        return $this->filetype;
    }

    /**
     * Implements \LORIS\Data\DataInstance interface
     *
     * @return array which can be serialized by json_encode()
     */
    public function jsonSerialize() : array
    {
        return [
                'fileid'              => $this->fileid,
                'filename'            => $this->filename,
                'filelocation'        => $this->filelocation,
                'outputtype'          => $this->outputtype,
                'acquisitionprotocol' => $this->acquisitionprotocol,
                'filetype'            => $this->filetype,
               ];
    }

    /**
     * Returns the CenterID for this row, for filters such as
     * \LORIS\Data\Filters\UserSiteMatch to match again.
     *
     * @return \CenterID
     */
    public function getCenterID(): \CenterID
    {
        return $this->centerid;
    }

    /**
     * This tells is the image is a phantom.
     *
     * @return bool True is the Entity_type is a scanner
     */
    public function isPhantom(): bool
    {
        return $this->entitytype === 'Scanner';
    }

    /**
     * Accessor for NIfTI filename stored in parameter_file.
     *
     * @return ?string
     */
    public function getCheckNiiFilename(): ?string
    {
        return $this->check_nii_filename;
    }

    /**
     * Accessor for NIfTI BVAL filename.
     *
     * @return ?string
     */
    public function getCheckBvalFilename(): ?string
    {
        return $this->check_bval_filename;
    }

    /**
     * Accessor for NIfTI BVEC filename.
     *
     * @return ?string
     */
    public function getCheckBvecFilename(): ?string
    {
        return $this->check_bvec_filename;
    }

    /**
     * Accessor for BIDS JSON filename.
     *
     * @return ?string
     */
    public function getBidsJsonFilename(): ?string
    {
        return $this->bids_json_filename;
    }
}
